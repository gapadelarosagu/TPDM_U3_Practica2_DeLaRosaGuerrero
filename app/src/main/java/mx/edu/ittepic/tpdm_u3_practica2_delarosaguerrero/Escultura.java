package mx.edu.ittepic.tpdm_u3_practica2_delarosaguerrero;

public class Escultura {
    private String nombre,autor,material;
    private int anio;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String tecnica) {
        this.material = tecnica;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }
}
